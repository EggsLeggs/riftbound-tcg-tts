function noScript()
  if prevT==nil then prevT=os.time()-5 end
  nowT=os.time()
  if nowT-prevT>0.3 then
    nClick=1
    prevT=nowT
    return
  else
    if nClick==nil then nClick=0 end
    nClick=nClick+1
  end
  if nClick<2 then return end
  self.clearButtons()
end

function getTraitors(obj,ply)
  if prevT==nil then prevT=os.time()-5 end
  nowT=os.time()
  if nowT-prevT>0.3 then
    nClick=1
    prevT=nowT
    return
  else
    if nClick==nil then nClick=0 end
    nClick=nClick+1
  end
  if nClick<2 then return end

  local pos=self.getPosition()+self.getTransformUp()+self.getTransformForward():scale(2)
  local rot=self.getRotation()
  rot[3]=180
  traitorDeck=spawnObjectJSON({json=traitorJSON,position=pos,rotation=rot})
  math.randomseed(os.time())
  for i=1,100+math.random(100) do math.random() end
  traitorDeck.shuffle()
  traitorDeck.shuffle()
  traitorDeck.shuffle()
  traitorDeck.shuffle()
  traitorDeck.shuffle()
  for n=1,4 do
    c=traitorDeck.takeObject()
    c.deal(1,ply,1)
    if c.getName():lower():find('gatekeeper') then
      local token1=spawnObjectJSON({json=gatekeeperToken,position=pos,rotation=rot})
      token1.deal(1,ply,1)
    end
    if c.getName():lower():find('grenadier') then
      local token2=spawnObjectJSON({json=grenadierToken,position=pos,rotation=rot})
      token2.deal(1,ply,1)
    end
  end
  traitorDeck.destruct()
end